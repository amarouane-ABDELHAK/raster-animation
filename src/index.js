class MeasureButtonControl {
    onClick() {
        $("#layer-measure").toggleClass("mapbox-gl-draw_line fa-stop");
        measureToggled = !measureToggled;
        if (measureToggled) {
            removeLayers(source_line_id, [layer_line_id]);
            $("div.distance-computed").css({ 'display': 'none' });
        }
    }
    onAdd(map) {
        this.map = map;
        this.container = document.createElement("div");
        this.container.className = "mapboxgl-ctrl mapboxgl-ctrl-group";
        this.container.addEventListener("contextmenu", (e) => e.preventDefault());
        this.container.addEventListener("click", (e) => this.onClick());
        this.container.innerHTML =
            '<div class="tools-box">' +
            "<button>" +
            '<span id="layer-measure" class="mapboxgl-ctrl-icon btn fa mapbox-gl-draw_line" aria-hidden="true" title="Measure Distance"></span>' +
            "</button>" +
            "</div>";
        return this.container;
    }
    onRemove() {
        this.container.parentNode.removeChild(this.container);
        this.map = undefined;
    }
}

map.addControl(new mapboxgl.ScaleControl());
map.addControl(new MeasureButtonControl());

function showHideLayers(layersIds, show) {
    layersIds.forEach(layerId => {
        if (map.getLayer(layerId)) {
            map.setLayoutProperty(
                layerId,
                "visibility",
                show ? "visible" : "none"
            );
        }
    });
}

function removeLayers(sourceId, layersIds) {
    if (map.getSource(sourceId)) {
        layersIds.forEach(layerId => {
            if (map.getLayer(layerId)) {
                map.removeLayer(layerId);
            }
        });
        map.removeSource(sourceId);
    }
}
function sleep(ms) {
    return new Promise(resolve => setTimeout(resolve, ms));
}

async function addRaster(itemProps, previousLayer) {
    console.log("Adding Raster");
    const collection = "goes-ch4"; //"lpjeosim-wetlandch4-daygrid-v2";
    const assets = "rad"; // "ensemble-mean-ch4-wetlands-emissions";
    const sourceId = "raster-source-" + itemProps.id;
    const layer_id = "raster-layer-" + itemProps.id;

    const TILE_URL = 
        `https://dev.ghg.center/api/raster/collections/${collection}/items/${itemProps.id}/tiles/WebMercatorQuad/{z}/{x}/{y}` +
        "?assets=" +
        assets +
        "&bidx=1&colormap_name=plasma&rescale=" +
        VMIN +
        "%2C" +
        VMAX ;

    return new Promise(async (resolve, reject) => {
        try {
            if (!map.getSource(sourceId)) {
                map.addSource(sourceId, {
                    type: "raster",
                    tiles: [TILE_URL],
                    tileSize: 256,
                    bounds: itemProps.bbox,
                });
            }

            if (!map.getLayer(layer_id)) {
                map.addLayer({
                    id: layer_id,
                    type: "raster",
                    source: sourceId,
                    paint: {},
                });
            }



            // Introduce a delay of 3 seconds
            await sleep(1000);

            if (previousLayer) {
                if (map.getLayer(previousLayer)) {
                    map.removeLayer(previousLayer);
                }
            }
            previous_layer_id = layer_id

            resolve(); // Resolve the promise after waiting for 3 seconds
        } catch (error) {
            reject(error); // Reject the promise if there is an error
        }
    });
}



function compute_distance(coordinates) {
    removeLayers(source_line_id, [layer_line_id]);
    points.push(coordinates);
    const markerEl = document.createElement("div");
    markerEl.className = "marker";
    const marker = new mapboxgl.Marker(markerEl)
        .setLngLat(coordinates)
        .addTo(map);
    markers.push(marker);
    if (points.length === 2) {
        const distance = calculateDistance(points[0], points[1]);
        const midPoint = [(points[0][0] + points[1][0]) / 2, (points[0][1] + points[1][1]) / 2];
        markers.forEach(marker => marker.remove());
        markers = [];

        if (distanceMarker) {
            distanceMarker.remove();
        }

        const distanceElement = document.createElement("div");
        distanceElement.className = "distance-computed";
        distanceElement.style.display = "block";
        distanceElement.style.padding = "5px";
        distanceElement.style.borderRadius = "5px";
        distanceElement.style.color = "white";
        distanceElement.innerHTML = `${distance.toFixed(2)} NM`;

        distanceMarker = new mapboxgl.Marker({
            element: distanceElement,
            anchor: 'bottom'
        })
            .setLngLat(midPoint)
            .addTo(map);

        const lineData = {
            type: "Feature",
            geometry: {
                type: "LineString",
                coordinates: points
            }
        };

        if (map.getSource(source_line_id)) {
            map.getSource(source_line_id).setData(lineData);
        } else {
            map.addSource(source_line_id, { type: "geojson", data: lineData });
            map.addLayer({
                id: layer_line_id,
                type: "line",
                source: source_line_id,
                layout: {
                    "line-join": "round",
                    "line-cap": "round"
                },
                paint: {
                    "line-color": "blue",
                    "line-width": 2,
                    "line-dasharray": [2, 4]
                }
            });
        }

        points = []; // Reset points for new measurements
    }
}

