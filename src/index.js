const playPauseButton = document.getElementById('play-pause');
const info = document.getElementById('info');
const timeline = document.getElementById('timeline');
let isPlaying = false;
let animationFrame;



async function updateMap(time) {
    console.log(time);
    if (time < itemProps.length) {
        const currentItem = itemProps[time];
        info.textContent = currentItem.id;
        await addRaster(currentItem, previous_layer_id);
    
    }

  
}

 async function play() {
    isPlaying = true;
    playPauseButton.textContent = 'Pause';
    let time =  parseInt(timeline.value);

    async function animate() {
        if (isPlaying && time <= parseInt(timeline.max)) {
            await updateMap(time);
            timeline.value = time;
            time++;
            animationFrame = requestAnimationFrame(animate);
        } else {
            await pause();
        }
    }
    await animate();
}

async function pause() {
    isPlaying = false;
    playPauseButton.textContent = 'Play';
    await cancelAnimationFrame(animationFrame);
}

playPauseButton.addEventListener('click', async () => {
    if (isPlaying) {
        await pause();
    } else {
        await play();
    }
});

timeline.addEventListener('change', (e) => {
    const time = parseInt(e.target.value);
    updateMap(time);
});


